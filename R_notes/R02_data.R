# 数据类型

# 字符型
a1 <- '123'
a2 <- 'zhangyiju'
a3 <- 'TRUE'
a4 <- 'FALSE'

# 数值型
b1 <- 1
b2 <- 666

# 逻辑型
c1 <- TRUE
c2 <- FALSE

# 判断数据类型
# 判断数值型
is.numeric(a1)
is.numeric(b1)
# 判断字符型
is.character(a1)
is.character(b1)
# 判断逻辑型
is.logical(TRUE)

# 数据类型转换
as.numeric()
as.character()
as.logical()

# 特殊值
is.na(NA)  # 数据缺失
is.null(NULL)  # 空值
is.nan(NaN)  # 
